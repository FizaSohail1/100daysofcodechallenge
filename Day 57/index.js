// question : What will be the result for these expressions?

5 > 4
"apple" > "pineapple"
"2" > "12"
undefined == null
undefined === null
null == "\n0\n"
null === +"\n0\n"

// Solution
5 > 4     // true
"apple" > "pineapple"  // false
"2" > "12"    //true
undefined == null //true
undefined === null  //false
null == "\n0\n" //false
null === +"\n0\n"  //false